<template>
  <v-app id="inspire">

    <!--flow list-->
    <flow-list ref="flowList"/>

    <!--toolbar-->
    <v-toolbar color="grey lighten-4" app absolute clipped-left>
      <v-toolbar-side-icon @click.native="refs('flowList').click()"></v-toolbar-side-icon>
      <v-toolbar-title>
        <i class="flow-icon flow-icon-logo"/>
      </v-toolbar-title>
      <v-spacer></v-spacer>
      <v-menu
        :close-on-content-click="false"
        :nudge-width="200"
        offset-y
        :nudge-bottom="14"
      >
        <v-btn icon slot="activator">
          <i class="material-icons">dvr</i>
        </v-btn>
        <AgentMessage></AgentMessage>
      </v-menu>
      <v-menu offset-y :nudge-bottom="14">
        <v-btn
          slot="activator"
          icon
        >
          <v-icon class="material-icons">account_circle</v-icon>
        </v-btn>
        <v-list>
          <v-list-tile
            v-for="(item, index) in items"
            :key="index"
            @click="settings(item.title)"
          >
            <v-icon small class="material-icons mr-4">{{item.icon}}</v-icon>
            <v-list-tile-title class="caption font-weight-regular">{{ item.title }}</v-list-tile-title>
          </v-list-tile>
        </v-list>
      </v-menu>
    </v-toolbar>

    <!--content-->
    <v-content>
      <v-container fluid fill-height>
        <router-view/>
      </v-container>
    </v-content>

    <!--btn for add flow-->
    <v-fab-transition>
      <v-btn
        fab
        bottom
        right
        color="pink"
        dark
        fixed
        @click.native="openCreateFlow"
        class="btn-add-flow">
        <v-icon>add</v-icon>
      </v-btn>
    </v-fab-transition>

    <v-layout row justify-center>
      <create-flow ref="createFlow"></create-flow>
    </v-layout>

    <!--footer-->
    <v-footer class="caption" app>
      <div class="ml-4">
        感谢使用 flow.ci 进行构建
      </div>
      <div class="ml-4">
        <a href='https://github.com/FlowCI/flow-web/issues'
           target='_blank' rel='noopener'>社区</a>
      </div>
      <div class="ml-4">
        <a href='https://docs.flow.ci'
           target='_blank' rel='noopener'>文档</a>
      </div>
      <v-spacer/>
      <div class="mr-4">
        Version 1.0 beta
      </div>
    </v-footer>
  </v-app>
</template>

<script>
  import FlowList from '@/components/FlowList'
  import CreateFlow from '@/components/CreateFlow/Index'
  import SockJS from 'sockjs-client'
  import Stomp from 'stompjs'
  import Actions from '@/api/store/actions'
  import AgentMessage from '@/components/Agent/AgentMessage'
  export default {
    name: 'App',
    components: {
      CreateFlow,
      FlowList,
      AgentMessage
    },
    data () {
      return {
        items: [
          { title: '个人设置', icon: 'account_circle' },
          { title: '系统管理', icon: 'settings' }
        ]
      }
    },
    created () {
      let self = this
      // 建立连接对象
      // 连接服务端提供的通信接口，连接以后才可以订阅广播消息和个人消息
      const socket = new SockJS(`${process.env.FLOWCI_API_URL}/ws`)
      // 获取STOMP子协议的客户端对象
      let stompClient = Stomp.over(socket)
      // 取消控制台 debug 日志
      stompClient.debug = function () { }
      stompClient.connect({}, function (frame) {
        self.$store.dispatch(Actions.Socket.SocketClient, stompClient)
        const path = '/topic/jobs'
        stompClient.subscribe(path, function (data) {
          self.$store.dispatch(Actions.Jobs.JobsStatus, JSON.parse(data.body))
        })
      })
    },
    methods: {
      openCreateFlow () {
        this.refs('createFlow').open()
      },
      refs (name) {
        return this.$refs[name]
      },
      settings (title) {
        if (title === '个人设置') {
          this.$router.push({path: '/admin/personalsetting'})
        } else if (title === '系统管理') {
          this.$router.push({path: '/admin/systemmanagement'})
        }
      }
    }
  }
</script>

